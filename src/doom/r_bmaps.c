const byte *R_BrightmapForTexName (const char *texname)
{
    if (vanillaparm)
    {
        return nobrightmap;
    }

    for (int i = 0; i < arrlen(fullbright_walls); i++)
    {
        const fullbright_t *fullbright = &fullbright_walls[i];

        if ((gamemission == doom && fullbright->game == DOOM2ONLY)
        ||  (gamemission != doom && fullbright->game == DOOM1ONLY))
        {
            continue;
        }

        if (!strncasecmp(fullbright->texture, texname, 8))
        {
            return fullbright->colormask;
        }
    }

    return nobrightmap;
}

// -----------------------------------------------------------------------------
// [crispy] brightmaps for sprites
// -----------------------------------------------------------------------------

const byte *R_BrightmapForSprite (const int type)
{
    if (brightmaps && !vanillaparm)
    {
        switch (type)
        {
            // Armor Bonus
            case SPR_BON2:
            // Cell Charge
            case SPR_CELL:
            {
                return greenonly2;
                break;
            }
            // Barrel
            case SPR_BAR1:
            {
                return greenonly3;
                break;
            }
            // Cell Charge Pack
            case SPR_CELP:
            // Floor Lamp
            case SPR_COLU:
            // Burning Barrel
            case SPR_FCAN:
            {
                return yellowonly;
                break;
            }
            // BFG9000
            case SPR_BFUG:
            // Plasmagun
            case SPR_PLAS:
            {
                return redonly;
                break;
            }
            // Candlestick
            case SPR_CAND:
            // Candelabra
            case SPR_CBRA:
            {
                return candle;
                break;
            }
            // Tall Blue Torch
            case SPR_TBLU:
            // Tall Green Torch
            case SPR_TGRN:
            // Tall Red Torch
            case SPR_TRED:
            // Short Blue Torch
            case SPR_SMBT:
            // Short Green Torch
            case SPR_SMGT:
            // Short Red Torch
            case SPR_SMRT:
            // Tall Technocolumn
            case SPR_TLMP:
            // Short Technocolumn
            case SPR_TLP2:
            {
                return light_sources;
                break;
            }
            // Evil Eye
            case SPR_CEYE:
            {
                return greenonly1;
                break;
            }
            // Floating Skull Rock
            case SPR_FSKU:
            // Pile of Skulls and Candles
            case SPR_POL3:
            {
                return fullbright;
                break;
            }
        }
    }
    else
    {
        switch (type)
        {
            case SPR_FCAN:
            case SPR_CAND:
            case SPR_CEYE:
            case SPR_FSKU:
            case SPR_CBRA:
            case SPR_COLU:
            case SPR_TLMP:
            case SPR_TLP2:
            case SPR_TBLU:
            case SPR_TGRN:
            case SPR_TRED:
            case SPR_SMBT:
            case SPR_SMGT:
            case SPR_SMRT:
            {
                return fullbright;
                break;
            }
        }
    }

    return nobrightmap;
}

